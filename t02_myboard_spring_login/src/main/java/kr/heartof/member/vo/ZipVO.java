package kr.heartof.member.vo;

public class ZipVO {
	private int zip_id;
	private String city;
	private String state;
	/**
	 * @return the zip_id
	 */
	public int getZip_id() {
		return zip_id;
	}
	/**
	 * @param zip_id the zip_id to set
	 */
	public void setZip_id(int zip_id) {
		this.zip_id = zip_id;
	}
	/**
	 * @return the city
	 */
	public String getCity() {
		return city;
	}
	/**
	 * @param city the city to set
	 */
	public void setCity(String city) {
		this.city = city;
	}
	/**
	 * @return the state
	 */
	public String getState() {
		return state;
	}
	/**
	 * @param state the state to set
	 */
	public void setState(String state) {
		this.state = state;
	}
}
